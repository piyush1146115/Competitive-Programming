SinglyLinkedListNode* reverse(SinglyLinkedListNode* llist) {
    SinglyLinkedListNode* prev;
    SinglyLinkedListNode* next;
    SinglyLinkedListNode* cur;
    
    cur = llist;
    prev = NULL;
    while(cur != NULL){
        next = cur->next;
        cur->next=prev;
        prev = cur;
        cur = next;
        
    }
    return prev;
    
}

